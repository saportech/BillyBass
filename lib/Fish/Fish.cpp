Fish::Fish(int fishType) : type(fishType) {
    switch (type) {
        case 1:
            tailHeadPin1 = 5;
            tailHeadPin2 = 18;
            mouthPin1 = 19;
            mouthPin2 = 4;
            txPin = 21;
            break;
        case 2:
            tailHeadPin1 = 26;
            tailHeadPin2 = 25;
            mouthPin1 = 33;
            mouthPin2 = 32;
            txPin = 22;
            break;
        case 3:
            tailHeadPin1 = 13;
            tailHeadPin2 = 12;
            mouthPin1 = 14;
            mouthPin2 = 27;
            txPin = 23;
            break;
        default:
            Serial.println("Invalid fish type!");
            break;
    }

    int PWMPin = 2;
    ledcSetup(0, 20000, 8);
    ledcAttachPin(PWMPin, 0);
    ledcWrite(0, 100);

    state = 0;
    previousMillisSM = 0;
    isFirstPerformCall = true;

}

void Fish::performAction(unsigned long duration, int action) {
  Action newAction = {duration, action};
  actionQueue.push(newAction);
}

